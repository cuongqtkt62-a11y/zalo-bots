/**
 * Lấy kịch bản Nhàu Tâm An cho ngày và thời điểm cụ thể.
 * @param {string} timeOfDay - 'Buổi sáng (8:00)' | 'Buổi trưa (12:00)' | 'Buổi tối (19:00)' hoặc tương đương
 * @returns {{ post: string, quote: string } | null}
 */
export function getNhauScenario(timeOfDay) {
  const dayOfWeek = new Date().getDay(); // 0=CN, 1=T2, ...
  const dayData = nhauScenarios[dayOfWeek];
  if (!dayData) return null;

  const t = (timeOfDay || '').toLowerCase();
  if (t.includes('sáng') || t.includes('8:') || t.includes('6:30')) {
    return dayData.morning;
  } else if (t.includes('trưa') || t.includes('12:')) {
    return dayData.noon;
  } else if (t.includes('tối') || t.includes('19:')) {
    return dayData.evening;
  }
  return null;
}

export default nhauScenarios;
